#include<unistd.h>

int	main(int argc, char **argv)
{
	int	j;

	j = 0;
	if (argc == 2)
	{
		while (argv[1][j])
		{
			if ((argv[1][j] >= 97 && argv[1][j] <= 109)
			|| (argv[1][j] >= 65 && argv[1][j] <= 77))
				argv[1][j] += 13;
			else if ((argv[1][j] >= 110 && argv[1][j] <= 122)
			|| (argv[1][j] >= 78 && argv[1][j] <= 90))
				argv[1][j] -= 13;
			write (1, &argv[1][j], 1);
			j++;
		}
	}
	write (1, "\n", 1);
}
